const diaDaSemana = 3;

switch (diaDaSemana) {
    case 1:
        console.log("hoje é Segunda")
    break;
    case 2:
        console.log("hoje é Terça")
    break;
    case 3:
        console.log("hoje é Quarta")
    break;
    case 4:
        console.log("hoje é Quinta")
    break;
    case 5:
        console.log("hoje é Sexta")
    break;
    case 6:
        console.log("hoje é Sabado")
    break;
    case 7:
        console.log("hoje é Domingo")
    break;
    default:
        console.log("Esse dia não existe, tente um número de 1 a 7")
}